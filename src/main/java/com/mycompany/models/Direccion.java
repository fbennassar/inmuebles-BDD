package com.mycompany.models;

public class Direccion {

	private int id;
	private String detalle;
	private int idCalle;
	private int idAvenida;
	private int idMunicipio;
	private int idEstado;
	private int idCodigoPostal;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDetalle() {
		return detalle;
	}
	public void setDetalle(String detalle) {
		this.detalle = detalle;
	}
	public int getIdCalle() {
		return idCalle;
	}
	public void setIdCalle(int idCalle) {
		this.idCalle = idCalle;
	}
	public int getIdAvenida() {
		return idAvenida;
	}
	public void setIdAvenida(int idAvenida) {
		this.idAvenida = idAvenida;
	}
	public int getIdMunicipio() {
		return idMunicipio;
	}
	public void setIdMunicipio(int idMunicipio) {
		this.idMunicipio = idMunicipio;
	}
	public int getIdEstado() {
		return idEstado;
	}
	public void setIdEstado(int idEstado) {
		this.idEstado = idEstado;
	}
	public int getIdCodigoPostal() {
		return idCodigoPostal;
	}
	public void setIdCodigoPostal(int idCodigoPostal) {
		this.idCodigoPostal = idCodigoPostal;
	}
	
	
}
